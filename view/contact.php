<script>
    document.getElementById('contact-form').addEventListener('submit', function(e) {
        if (e.preventDefault) e.preventDefault();
        const form = this;
        const submitBtn = form.querySelector('button[type="submit"]');
        const submitText = document.getElementById('submit-text');

        submitBtn.disabled = true;
        const originalText = submitText.innerText;
        submitText.innerText = "Authenticating...";

        grecaptcha.ready(function() {
            grecaptcha.execute('6LeIxAcTAAAAAJcZVRqyHh71UMIEGNQ_MXjiZKhI', {
                action: 'submit'
            }).then(function(token) {
                const formData = new FormData(form);
                formData.append('recaptcha_token', token);
                submitText.innerText = "Transmitting...";

                fetch(`${window.GA_BASE_URL}/api/contact_handler.php`, {
                        method: 'POST',
                        body: formData
                    })
                    .then(response => response.json())
                    .then(data => {
                        if (data.success) {
                            Toast.fire({
                                icon: 'success',
                                title: data.message
                            });
                            form.reset();
                        } else {
                            Toast.fire({
                                icon: 'error',
                                title: data.message
                            });
                        }
                    })
                    .catch(error => {
                        console.error('Error:', error);
                        Toast.fire({
                            icon: 'error',
                            title: 'Sacred connection lost. Please try again.'
                        });
                    })
                    .finally(() => {
                        submitBtn.disabled = false;
                        submitText.innerText = originalText;
                    });
            });
        });
    });
</script>